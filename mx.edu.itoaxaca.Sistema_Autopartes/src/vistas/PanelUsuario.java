    private void btnOkBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkBuscarActionPerformed
        // TODO add your handling code here:
        limpiarCampos();

        String claveaBuscar = null;
        int aux = 0;
        try{
            claveaBuscar=campoidBuscar.getText();
            aux = Integer.parseInt(claveaBuscar);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Ingresa un código valido");
        }

        if(claveaBuscar!=null){
            busqueda = ujc.findUsuario(aux);
            if(busqueda!=null){
                campoidBuscar.setEnabled(true);
                campoidBuscar.setText(busqueda.getUsId()+"");

                String sex = busqueda.getSexo();
                if(sex.equals("hombre")){comboSexo.setSelectedIndex(0); }else{comboSexo.setSelectedIndex(1);}
                textId.setText(busqueda.getUsId()+"");
                textNom.setText(busqueda.getNombres());
                textDir.setText(busqueda.getDireccion());
                textAp1.setText(busqueda.getApP()+"");
                textAp2.setText(busqueda.getApM()+"");
                textPassword.setText(busqueda.getContrasena());
                textTel.setText(busqueda.getTelefono());
                String car = busqueda.getCargo();
                if(car.equals("administrador")){comboCargo.setSelectedIndex(0); }else{comboCargo.setSelectedIndex(1);}
                String image = busqueda.getImagen();
                src = image;
                panelImg.removeAll();
                if(image.length()<1){image = "nofoto.jpg";}
                imagen = new Imagen(image);
                panelImg.add(imagen);
                panelImg.repaint();

                btnEditar.setEnabled(true);
                btnEliminar.setEnabled(true);
            }else JOptionPane.showMessageDialog(null,"No existe Usuario con ese código");
        }
    }//GEN-LAST:event_btnOkBuscarActionPerformed

