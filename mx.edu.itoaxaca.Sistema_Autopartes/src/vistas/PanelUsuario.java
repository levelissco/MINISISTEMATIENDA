    public void listarDatos(){
      String encabezado[]={"Codigo","Nombre","Apellido1","Apellido2","Sexo","Contraseña","Imagen","Telefono","Direccion","Cargo"};
        DefaultTableModel modelo=new DefaultTableModel(encabezado,0);
        
        tablaArt.setModel(new DefaultTableModel());
         List <Usuario> array=ujc.findUsuarioEntities();
         
         for(int x=0;x<array.size();x++){
              int id = array.get(x).getUsId();
              String nom = array.get(x).getNombres();
              String ap_P = array.get(x).getApP();
              String ap_M = array.get(x).getApM();
              String sex = array.get(x).getSexo();
              String contra = array.get(x).getContrasena();
              String imagen = array.get(x).getImagen();
              String tel = array.get(x).getTelefono();
              String dir = array.get(x).getDireccion();
              String cargo  = array.get(x).getCargo();
             
             Object [] data={id,nom,ap_P,ap_M,sex,contra,imagen,tel,dir,cargo};
             modelo.addRow(data);
         }
         
         tablaArt.setModel(modelo);
    }
    
